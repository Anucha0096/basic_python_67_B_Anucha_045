import math 

x = min(5, 10, 15)
y = max(5, 10, 15)
print(x, y)

z = abs(-7.25) # |-7.25| = 7.25 เป็นค่าบวกเสมอ
print(z)

v = pow(4, 3) #ยกกำลัง
print(v)

pi = math.pi #ค่าพาย
print(pi)

s_root = math.sqrt(64)
print(s_root)

x = math.ceil(1.3)
y = math.floor(1.3)
print(x)
print(y)